def check_critical_defects(data):
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ –ü—Ä–∏–ª–æ–∂–µ–Ω–∏—é –ê –ì–û–°–¢ 34583-2019"""
    critical = []
    
    # –ü—Ä–∏–º–µ—Ä—ã –∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏—Ö –¥–µ—Ñ–µ–∫—Ç–æ–≤
    if not data.get("–î–≤—É—Å—Ç–æ—Ä–æ–Ω–Ω—è—è —Å–≤—è–∑—å", True):
        critical.append("–û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –¥–≤—É—Å—Ç–æ—Ä–æ–Ω–Ω—è—è –ø–µ—Ä–µ–≥–æ–≤–æ—Ä–Ω–∞—è —Å–≤—è–∑—å")
    if data.get("–õ–æ–≤–∏—Ç–µ–ª–∏", "–ò—Å–ø—Ä–∞–≤–Ω—ã") == "–ù–µ–∏—Å–ø—Ä–∞–≤–Ω—ã":
        critical.append("–ù–µ–∏—Å–ø—Ä–∞–≤–Ω—ã –ª–æ–≤–∏—Ç–µ–ª–∏")
    if data.get("–¢—è–≥–æ–≤—ã–µ –∫–∞–Ω–∞—Ç—ã: –û–±—Ä—ã–≤—ã", False):
        critical.append("–û–±—Ä—ã–≤—ã —Ç—è–≥–æ–≤—ã—Ö –∫–∞–Ω–∞—Ç–æ–≤")
    if data.get("–ö–∞–Ω–∞—Ç—ã –æ–≥—Ä–∞–Ω–∏—á–∏—Ç–µ–ª—è —Å–∫–æ—Ä–æ—Å—Ç–∏: –ù–∞—Ç—è–∂–µ–Ω–∏–µ") == "–û—Å–ª–∞–±–ª–µ–Ω":
        critical.append("–û—Å–ª–∞–±–ª–µ–Ω –∫–∞–Ω–∞—Ç –æ–≥—Ä–∞–Ω–∏—á–∏—Ç–µ–ª—è —Å–∫–æ—Ä–æ—Å—Ç–∏")
    if data.get("–¢—è–≥–æ–≤—ã–µ –∫–∞–Ω–∞—Ç—ã: –ò–∑–Ω–æ—Å, %", 100) < 70:
        critical.append("–ò–∑–Ω–æ—Å —Ç—è–≥–æ–≤—ã—Ö –∫–∞–Ω–∞—Ç–æ–≤ >30%")
        
    return critical

def recommend_remaining_life(data, critical_defects):
    """–ü—Ä–æ—Å—Ç–µ–π—à–∞—è —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—è (–≤ —Ä–µ–∞–ª—å–Ω–æ–º –ø—Ä–æ–µ–∫—Ç–µ ‚Äî ML-–º–æ–¥–µ–ª—å)"""
    if critical_defects:
        return -1.0  # –¢—Ä–µ–±—É–µ—Ç—Å—è –∑–∞–ø—Ä–µ—Ç
    
    # –ë–∞–∑–æ–≤–∞—è –ª–æ–≥–∏–∫–∞ (–º–æ–∂–Ω–æ –∑–∞–º–µ–Ω–∏—Ç—å –Ω–∞ RandomForest)
    wear = data.get("–¢—è–≥–æ–≤—ã–µ –∫–∞–Ω–∞—Ç—ã: –ò–∑–Ω–æ—Å, %", 100)
    if wear >= 90:
        return 8.0
    elif wear >= 80:
        return 5.0
    elif wear >= 70:
        return 2.0
    else:
        return 0.5

